# Парсинг данных
rects = []
for line in data.strip().split('\n'):
    parts = line.split()
    if len(parts) == 5:
        h, x, y, X, Y = map(int, parts)
        rects.append({'h': h, 'x': x, 'y': y, 'X': X, 'Y': Y})

# Находим диапазон высот для цветовой шкалы
h_min = min(r['h'] for r in rects)
h_max = max(r['h'] for r in rects)

# Создаём фигуру
fig, ax = plt.subplots(1, 1, figsize=(14, 10))

# Цветовая карта
cmap = cm.winter

# Рисуем прямоугольники (сначала низкие, потом высокие — чтобы высокие были сверху)
rects_sorted = sorted(rects, key=lambda r: r['h'])

for rect in rects_sorted:
    x, y, X, Y, h = rect['x'], rect['y'], rect['X'], rect['Y'], rect['h']
    width = X - x
    height = Y - y

    # Нормализуем высоту для цвета
    if h_max > h_min:
        norm_h = (h - h_min) / (h_max - h_min)
    else:
        norm_h = 0.5

    color = cmap(norm_h)

    # Создаём прямоугольник
    patch = patches.Rectangle(
        (x, y), width, height,
        linewidth=0.5,
        edgecolor='black',
        facecolor=color,
        alpha=0.7
    )
    ax.add_patch(patch)

    # Добавляем текст с высотой (только для достаточно больших прямоугольников)
    if width > 50 and height > 30:
        ax.text(
            x + width/2, y + height/2,
            str(h),
            ha='center', va='center',
            fontsize=7,
            color='white' if norm_h > 0.5 else 'black'
        )

# Настройки осей
ax.set_xlim(-10, 1250)
ax.set_ylim(-10, 850)
ax.set_aspect('equal')
ax.set_xlabel('X')
ax.set_ylabel('Y')
ax.set_title('Height Map Visualization')
ax.grid(True, alpha=0.3)

# Цветовая шкала
sm = plt.cm.ScalarMappable(cmap=cmap, norm=plt.Normalize(vmin=h_min, vmax=h_max))
sm.set_array([])
cbar = plt.colorbar(sm, ax=ax, label='Height (h)')

plt.tight_layout()
plt.savefig('height_map.png', dpi=150)
plt.show()

print(f"Всего прямоугольников: {len(rects)}")
print(f"Диапазон высот: {h_min} - {h_max}")
